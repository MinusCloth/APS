package entidades;

public class Patrimonio {
	private int id_patrimonio,id_pessoa;  
	private String nome_Patrimonio;
	private String pn;
	private String sn;
	
	
	

	
	
	
	public String getNome_Patrimonio() {
		return nome_Patrimonio;
	}



	public void setNome_Patrimonio(String nome_Patrimonio) {
		this.nome_Patrimonio = nome_Patrimonio;
	}



	public String getPn() {
		return pn;
	}



	public void setPn(String pn) {
		this.pn = pn;
	}



	public String getSn() {
		return sn;
	}



	public void setSn(String sn) {
		this.sn = sn;
	}



	public int getId_patrimonio() {
		return id_patrimonio;
	}



	public void setId_patrimonio(int id_patrimonio) {
		this.id_patrimonio = id_patrimonio;
	}



	public int getId_pessoa() {
		return id_pessoa;
	}



	public void setId_pessoa(int id_pessoa) {
		this.id_pessoa = id_pessoa;
	}


}
